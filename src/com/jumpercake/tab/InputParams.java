package com.jumpercake.tab;

public class InputParams {
	
	private Double numGel;
	private Double percentGel;
	private Double percentArcy;
	
	public Double getNumGel() {
		return numGel;
	}
	public void setNumGel(Double numGel) {
		this.numGel = numGel;
	}
	public Double getPercentGel() {
		return percentGel;
	}
	public void setPercentGel(Double percentGel) {
		this.percentGel = percentGel;
	}
	public Double getPercentArcy() {
		return percentArcy;
	}
	public void setPercentArcy(Double percentArcy) {
		this.percentArcy = percentArcy;
	}
	
}
